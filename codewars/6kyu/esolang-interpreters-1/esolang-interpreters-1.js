function myFirstInterpreter(code) {
  const memory = [];
  
  code.split('.').reduce((acc, cur) => {
    acc += cur.match(/\+/g) ? cur.match(/\+/g).length : 0;
    memory.push(acc % 256);
    
    return acc;
  }, 0);
  
  return memory.map((e) => String.fromCharCode(e))
    .join('')
    .slice(0, -1);
}

